	#[rstest]
	#[tokio::test(flavor = "multi_thread")]
	#[serial(database)]
	async fn test_oauth_unlink_rejects_removing_last_sign_in_method(
		#[future] db: (
			ContainerAsync<GenericImage>,
			Arc<DatabaseConnection>,
			APIClient,
			ResolvedUrls,
		),
	) {
		// Arrange — OAuth-only user (no password) with a single social
		// link. Removing it would leave the account unreachable, so the
		// view must refuse with 422 instead of deleting.
		let (_c, _conn, _cli, _urls) = db.await;
		let user_id = seed_user_no_password("unlink_lockout", "lockout@example.test").await;
		seed_link(user_id, "github", "gh_lockout_1").await;
		let auth = AuthInfo(AuthState::authenticated(user_id.to_string(), false, true));

		// Act
		let result = oauth_unlink_original(Path("github".to_string()), auth).await;

		// Assert — Validation error AND the link is still present in
		// storage afterward, proving the refusal happened before the
		// delete.
		match result {
			Err(AppError::Validation(msg)) => {
				assert!(
					msg.contains("set a password before unlinking"),
					"lockout message should guide the user to set a password: {msg}"
				);
			}
			other => panic!("expected Validation, got {other:?}"),
		}
		let storage = OrmSocialAccountStorage::new();
		let links = storage.find_by_user(user_id).await.unwrap();
		assert_eq!(links.len(), 1, "lockout-protected link must NOT be deleted");
	}

	#[rstest]
	#[tokio::test(flavor = "multi_thread")]
	#[serial(database)]
	async fn test_oauth_unlink_succeeds_when_user_has_password(
		#[future] db: (
			ContainerAsync<GenericImage>,
			Arc<DatabaseConnection>,
			APIClient,
			ResolvedUrls,
		),
	) {
		// Arrange — user has a password, so removing the only social
		// link is safe and the lockout guard does not fire.
		let (_c, _conn, _cli, _urls) = db.await;
		let user_id = seed_user_with_password("unlink_ok", "ok@example.test").await;
		seed_link(user_id, "github", "gh_ok_1").await;
		let auth = AuthInfo(AuthState::authenticated(user_id.to_string(), false, true));

		// Act
		let response = oauth_unlink_original(Path("github".to_string()), auth)
			.await
			.expect("unlink should succeed when user has password");

		// Assert — 200 OK with JSON body, and the link is gone.
		assert_eq!(response.status, StatusCode::OK);
		let body =
			std::str::from_utf8(response.body.as_ref()).expect("response body must be utf-8");
		assert!(
			body.contains("\"success\":true") && body.contains("\"provider\":\"github\""),
			"response body should report success: {body}"
		);
		let storage = OrmSocialAccountStorage::new();
		let links = storage.find_by_user(user_id).await.unwrap();
		assert!(links.is_empty(), "link should have been deleted");
	}
