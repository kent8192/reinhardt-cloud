	//! Inline tests for `oauth_unlink`.
	//!
	//! The `_original` function generated by `#[post(...)]` is module-
	//! private, so view-level tests must live in the same source file as
	//! the view itself.
	//!
	//! Coverage map:
	//!   * malformed user_id  → 401 (`AppError::Authentication`) — no DB
	//!   * user-not-found     → 404 — DB
	//!   * no-link-to-provider → 404 — DB
	//!   * lockout protection  → 422 (`AppError::Validation`) — DB
	//!   * happy path          → 200 JSON — DB
	//!
	//! The DB-backed cases reuse the `postgres_with_migrations_from_dir`
	//! fixture used elsewhere in the auth integration suite.
	#![allow(clippy::too_many_lines)]

