	async fn seed_user_with_password(username: &str, email: &str) -> Uuid {
		let mut user = User::new(
			username.to_string(),
			email.to_lowercase(),
			String::new(),
			String::new(),
			None,
			true,
			false,
			false,
		);
		user.set_password("test-password-1234").unwrap();
		User::objects().create(&user).await.expect("seed user").id
	}

	async fn seed_user_no_password(username: &str, email: &str) -> Uuid {
		let user = User::new(
			username.to_string(),
			email.to_lowercase(),
			String::new(),
			String::new(),
			None,
			true,
			false,
			false,
		);
		User::objects().create(&user).await.expect("seed user").id
	}

	async fn seed_link(user_id: Uuid, provider: &str, sub: &str) {
		let storage = OrmSocialAccountStorage::new();
		let now = chrono::Utc::now();
		let acc = SocialAccount {
			id: Uuid::now_v7(),
			user_id,
			provider: provider.to_string(),
			provider_user_id: sub.to_string(),
			email: Some(format!("{sub}@example.test")),
			display_name: Some(sub.to_string()),
			picture: None,
			access_token: String::new(),
			refresh_token: None,
			token_expires_at: now + Duration::hours(1),
			scopes: Vec::new(),
			created_at: now,
			updated_at: now,
		};
		storage.create(acc).await.expect("seed link");
	}

	#[rstest]
	#[tokio::test]
	async fn test_oauth_unlink_rejects_malformed_user_id_in_token() {
		// Arrange — an authenticated AuthState whose user_id field is not
		// a valid UUID. This can happen if a token was minted for a non-
		// User principal and must surface as 401, not 500.
		let auth = AuthInfo(AuthState::authenticated("not-a-uuid", false, true));

		// Act
		let result = oauth_unlink_original(Path("github".to_string()), auth).await;

		// Assert
		match result {
			Err(AppError::Authentication(msg)) => {
				assert!(
					msg.contains("invalid user_id in token"),
					"error message should explain the parse failure: {msg}"
				);
			}
			other => panic!("expected Authentication error, got {other:?}"),
		}
	}

